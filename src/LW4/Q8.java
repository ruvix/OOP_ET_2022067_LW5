import java.util.Scanner;

public class Q8 {
    public static void main(String[] args) {


        System.out.print("Enter the number : ");

        Scanner scan = new Scanner(System.in);
        int num=scan.nextInt();

        for(int i=0+1 ; i<=10 ; i++){

            int multi =num*i;
            System.out.println(num+" * "+i+" = "+multi);

        }



























    }
}
